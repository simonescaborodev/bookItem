public class Book {

    private String bookTitle;
    private String bookAuthor;
    private String bookHouse;
    private String bookDescription;
    private String bookLanguage;
    private int bookEdition;
    private int bookPublishingYear;

    public String getBookTitle() {
        return bookTitle;
    }

    public void setBookTitle(String bookTitle) {
        this.bookTitle = bookTitle;
    }

    public String getBookAuthor() {
        return bookAuthor;
    }

    public void setBookAuthor(String bookAuthor) {
        this.bookAuthor = bookAuthor;
    }

    public String getBookHouse() {
        return bookHouse;
    }

    public void setBookHouse(String bookHouse) {
        this.bookHouse = bookHouse;
    }

    public String getBookDescription() {
        return bookDescription;
    }

    public void setBookDescription(String bookDescription) {
        this.bookDescription = bookDescription;
    }

    public String getBookLanguage() {
        return bookLanguage;
    }

    public void setBookLanguage(String bookLanguage) {
        this.bookLanguage = bookLanguage;
    }

    public int getBookEdition() {
        return bookEdition;
    }

    public void setBookEdition(int bookEdition) {
        this.bookEdition = bookEdition;
    }

    public int getBookPublishingYear() {
        return bookPublishingYear;
    }

    public void setBookPublishingYear(int bookPublishingYear) {
        this.bookPublishingYear = bookPublishingYear;
    }

}
